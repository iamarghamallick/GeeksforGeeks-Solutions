class Solution {
  public:
    vector<int> maxOfSubarrays(vector<int>& arr, int k) {
        multiset<int> m;
        vector<int> ans;
        
        for(int i = 0; i < k-1; i++){
            m.insert(arr[i]);
        }
        
        for(int i = k-1; i < arr.size(); i++){
            m.insert(arr[i]);
            ans.push_back(*(m.crbegin()));
            m.erase(m.find(arr[i-k+1]));
        }
        
        return ans;
    }
};


