class Solution {
  public:
    int carry = 1;
    
    void solve(Node* &head) {
        if(!head)   return;
        solve(head->next);
        int sum = head->data + carry;
        head->data = sum % 10;
        carry = sum / 10;
    }
    
    Node* addOne(Node* head) {
        solve(head);
        if(carry) {
            Node* newHead = new Node(1);
            newHead->next = head;
            return newHead;
        }
        return head;
    }
};

