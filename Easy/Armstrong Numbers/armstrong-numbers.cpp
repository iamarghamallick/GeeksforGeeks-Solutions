class Solution {
  public:
    string armstrongNumber(int n) {
        int org = n;
        int rev = 0;
        while(n) {
            int d = n % 10;
            rev += d * d * d;
            n /= 10;
        }
        
        return org == rev ? "true" : "false";
    }
};

